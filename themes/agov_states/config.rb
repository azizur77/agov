require 'fileutils'

states = {
  'nsw' => 'waratah',
  'act' => 'bluebell'
}


on_stylesheet_saved do |file|
  states.each do |state, symbol|
    if File.exists?(file) && File.basename(file) == state + ".css"
      FileUtils.mv(file, File.dirname(file) + "/../" + symbol + "/css/styles.css")
    end
  end
end